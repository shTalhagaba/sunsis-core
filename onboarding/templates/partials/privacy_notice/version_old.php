<h4 class="text-bold"><u>Privacy Notice</u></h4>

<p>
    This privacy notice is issued by the Education and Skills Funding Agency (ESFA) on behalf of the Secretary of 
    State for the Department of Education (DfE) to inform learners about the Individualised Learner Record (ILR) and 
    how their personal information is used in the ILR. Your personal information is used by the DfE to exercise our 
    functions under article 6(1)(e) of the UK GDPR and to meet our statutory responsibilities, including under the 
    Apprenticeships, Skills, Children and Learning Act 2009.
</p>
<p>
    The ILR collects data about learners and learning undertaken. Publicly funded colleges, training organisations, 
    local authorities, and employers (FE providers) must collect and return the data to the ESFA each year under the 
    terms of a funding agreement, contract or grant agreement. It helps ensure that public money distributed through 
    the ESFA is being spent in line with government targets. It is also used for education, training, employment, and well 
    being purposes, including research. We retain ILR learner data for 3 years for operational purposes and 66 years for 
    research purposes. For more information about the ILR and the data collected, please see the ILR specification 
    at <a href="https://www.gov.uk/government/collections/individualised-learner-record-ilr" target="_blank">https://www.gov.uk/government/collections/individualised-learner-record-ilr</a>
</p>
<p>
    ILR data is shared with third parties where it complies with DfE data sharing procedures and where the law allows it. 
    The DfE and the English European Social Fund (ESF) Managing Authority (or agents acting on their behalf) may contact 
    learners to carry out research and evaluation to inform the effectiveness of training.
</p>
<p>
    For more information about how your personal data is used and your individual rights, please see the DfE Personal Information 
    Charter (<a href="https://www.gov.uk/government/organisations/department-for-education/about/personal-information-charter" target="_blank">https://www.gov.uk/government/organisations/department-for-education/about/personal-information-charter</a>) 
    and the ESFA Privacy Notice (<a href="https://www.gov.uk/government/publications/esfa-privacy-notice" target="_blank">https://www.gov.uk/government/publications/esfa-privacy-notice</a>)
</p>

<p class="text-bold">2.0	 Off-the-job (OTJT) training</p>

<p>2.1	Off-the-job training is training received by the apprentice, during the apprentice's paid hours, for the purpose of achieving their apprenticeship. It is a statutory requirement for an English Apprenticeship. At least 20% of the apprentice's paid hours, over the planned duration of the apprenticeship (for standards this would be until the gateway for end-point assessment), must be spent on off-the-job training. This excludes off-the-job training related to the achievement of Maths or English qualifications.</p>

<p>2.2	Off-the-job training must be directly relevant to the apprenticeship framework or standard, teaching new knowledge, skills and behaviors required to reach competence in the particular occupation.  It is the responsibility of both the training provider and the employer to ensure that an apprentice spends a minimum of 20% of their employed time doing off the job training, as set out within the government's funding rules.</p>

<p>2.3	When calculating the required amount of off-the-job training, the apprentice's statutory leave entitlement should be deducted. Employees who work a 5-day week receive at least 28 days paid annual holiday (20 days plus 8 bank holidays). This is the statutory leave entitlement referred to in this paragraph and is the equivalent of 5.6 weeks of holiday.  This should be pro-rata up for an apprenticeship that exceeds 12 months (52 weeks).</p>

<p class="text-bold">Example for apprentice on a 12 month apprenticeship working 40 hours per week</p>

<p>(Weeks on programme - Statutory annual leave) x normal weekly hours x 0.2</p>
<p>(52 - 5.6) x 40 x 0.2 = 371</p>

<p class="text-bold"><br>Example for apprentice on a 24 month apprenticeship working 40 hours per week</p>

<p>(Weeks on programme - Statutory annual leave) x normal weekly hours x 0.2</p>
<p>(104 - 11.2) x 40 x 0.2 = 742</p>

<p class="text-bold"><br>This would be the planned minimum hours for off-the-job activity across the apprenticeship duration.</p>


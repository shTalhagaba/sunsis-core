        <h2 class="text-bold text-info">Sunesis New Releases 2024/25</h2>
        <p>We have released some new features within Sunesis from 1st August 2024.</p><br>

        <h4 class="text-bold text-info">End Info (Completing the ILR for all qualifications)</h4>
        <p>When you are due to complete the learners programme where all continuing learnings aims are required to be updated with the same end information, 
            just click the new tab called 'End Info'.</p>
        <img src="/images/release_notes/202425/fig1.PNG" alt="Fig. 1" srcset="ILR End Info"><br>
        <p>A pop-up box will appear. Complete all the required fields and click the 'Update' button. All continuing learning aims will be updated with this information, 
            no longer requiring you to click into each tab to update the data. Learning aims already completed will not be affected by this update. 
            This feature can be used for Completions, Achievements, Withdrawals and Break in Learning.</p>

        <br>
        <h4 class="text-bold text-info">Restart from BIL (Break in Learning)</h4>
        <p>For learners on a break in learning, a button on the top green menu bar will be available. Click this button to take you to the next screen to add data.</p>
        <img src="/images/release_notes/202425/fig2.PNG" alt="Fig. 2" srcset="Restart From BIL"><br>
        <p>Enter the required information (if you enter the Start Date, the system will autopopulate the End Dates, however you can override this information if required) 
            and click the Restart button on the top Green menu bar. All the relevant fields will be created for the Restart Training Record. 
            This button is only available for learners who are already on a break in learning.</p>

        <br>
        <h4 class="text-bold text-info">Tags</h4>
        <p>On Employers, Learners and Training Records, there is now a Tags feature. Tags provide users with the flexibility to group learners and employers in various 
            categories (multiple tags can be assigned to one learner/ employer) and then use filters to create reports, based on those categories. </p>
        <p class="text-bold">Employers</p>
        <img src="/images/release_notes/202425/fig3.PNG" alt="Fig. 3" srcset="Employers Tag Screen"><br>
        <p class="text-bold">Learners</p>
        <img src="/images/release_notes/202425/fig4.PNG" alt="Fig. 4" srcset="Learners Tag Screen"><br>
        <p class="text-bold">Training Records</p>
        <img src="/images/release_notes/202425/fig5.PNG" alt="Fig. 5" srcset="Training Records Tag Screen"><br>
        <p>You can create tags at each option.</p>
        <p>E.g. Employer</p>
        <ul style="margin-left: 10px;">
            <li>RAG Rating - GOLD</li>
            <li>Area</li>
            <li>Reliable</li>
            <li>Ready for enorlment</li>
        </ul>
        <p>E.g. Learner</p>
        <ul style="margin-left: 10px;">
            <li>Guidance needed</li>
            <li>Ready for enorlment</li>
        </ul>
        <p>Training Record Tags examples</p>
        <img src="/images/release_notes/202425/fig6.PNG" alt="Fig. 6" srcset="Training Record Tag Example"><br>
        <p>Once a tag is created, it will appear in the drop-down box for future choice.</p>
        <p>Using the Enter Tag box, you can create your own new tag which will become available in the drop-down box for future use.</p>
        <p>If you assign a tag to an Employer, Learner or Training Record, you can view those assigned tags (see the highlighted box below) as well as remove each, if 
            necessary, by clicking on the X to the right of each tag.</p>
        <img src="/images/release_notes/202425/fig7.PNG" alt="Fig. 7" srcset="Fig 7"><br>
        <p>Tags are also available on the Employer, Learner and Training Record filters to choose relevant records.</p>

        <br>
        <h4 class="text-bold text-info">CHOC (Change of Circumstances)</h4>
        <p>A new tab on the Training Record is available called CHOC</p>
        <img src="/images/release_notes/202425/fig8.PNG" alt="Fig. 8" srcset="Fig 8"><br>
        <p>This tab currently provides 4 options to change data. </p>
        <p>You can view any existing CHOC items and Add New</p>
        <img src="/images/release_notes/202425/fig9.PNG" alt="Fig. 9" srcset="Fig 9"><br>
        <p>Current information is displayed at the top of the screen.</p>
        <p>Click on the relevant option within the drop-down list in 'Select Type' and complete the required fields. Once you have completed the data, click the 
            Save button on the top green menu bar, to cascade the instructions to Sunesis.</p>

